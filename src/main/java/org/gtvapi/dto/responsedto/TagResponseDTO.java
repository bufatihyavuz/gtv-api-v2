package org.gtvapi.dto.responsedto;

public class TagResponseDTO {

    private Long id;
    private String name;

    public TagResponseDTO(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
